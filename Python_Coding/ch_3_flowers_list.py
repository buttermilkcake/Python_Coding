flowers = ['zinnia', 'marigold', 'rose', 'dahlia', 'poppy']

print("The first three items on the list are:")
for flower in flowers[:3]:
    print(flower)
    
print("Three flowers from the middle of the list are:")
for flower in flowers[1:4]:
    print(flower)

print("The last three flowers on the list are:")
for flower in flowers[-3:]:
    print(flower)
